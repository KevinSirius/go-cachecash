package util

import (
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestKeyedPRF(t *testing.T) {
	chunkIdx := uint64(34)
	seqNo := uint32(9)
	key := []byte{0x52, 0x39, 0xcd, 0xd3, 0x4e, 0xd4, 0x30, 0xf9, 0x6b, 0xe3, 0x59, 0x2b, 0xe7, 0x39, 0x4a, 0x09}

	iv, err := KeyedPRF(Uint64ToLE(chunkIdx), seqNo, key)

	assert.Nil(t, err)
	assert.Equal(t, []byte{0x28, 0xd6, 0x76, 0x87, 0x78, 0xc0, 0x25, 0x59, 0x1a, 0xd7, 0xa8, 0x8f, 0xe8, 0x91, 0x24, 0x16}, iv)
}

func TestUint64ToLE(t *testing.T) {
	chunkIdx := uint64(34)
	x := Uint64ToLE(chunkIdx)
	assert.Equal(t, []byte{0x22, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0}, x)
}
